//Write a program that can print even numbers from 1 to 100.


//syntex for For Loop(increment)
for(let x = 1; x < 101; x++) {//(let x = initialization),(x < 100= condition),(x++ is increment)
    console.log(x);
}



//syntex for While Loop(Increment)
let count = 1;
while(count < 101) {
    console.log(count)//value is 1
    count++ //increment by +1,2,3......
}
//for loop and while loop are both loop similar